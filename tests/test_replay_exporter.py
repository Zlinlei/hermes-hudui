def test_export_updates_skill_provenance_index_without_inventing_hashes(tmp_path, monkeypatch) -> None:
    monkeypatch.setenv("HERMES_HUD_REPLAY_DIR", str(tmp_path))
    detail = _skill_detail()

    export_json(detail)
    export_json(detail)
    index = get_skill_provenance_index()
    skill = index["skills"][0]

    assert skill["name"] == "auth-helper"
    assert skill["version"] is None
    assert skill["hash"] is None
    assert skill["runs_used_in"] == [detail.run.replay_id]
    assert skill["usage_count"] == 1
    assert skill["success_count"] == 1
    assert skill["success_rate"] == 1.0
    assert skill["first_used_at"] == detail.run.started_at
    assert skill["last_used_at"] == detail.run.started_at
    assert skill["mutation_receipts"] == []


def test_export_records_skill_mutation_receipts(tmp_path, monkeypatch) -> None:
    monkeypatch.setenv("HERMES_HUD_REPLAY_DIR", str(tmp_path))
    detail = _skill_mutation_detail()

    result = export_json(detail)
    export_json(detail)
    skill = get_skill_provenance_index()["skills"][0]
    receipts = skill["mutation_receipts"]

    assert skill["name"] == "auth-helper"
    assert len(receipts) == 1
    assert receipts[0]["event_type"] == "skill_created"
    assert receipts[0]["replay_id"] == detail.run.replay_id
    assert receipts[0]["receipt_hash"] == result.receipt_hash
    assert receipts[0]["redacted_replay_hash"] == result.redacted_replay_hash
