def test_publish_replay_copies_static_artifacts_to_local_visibility_dir(tmp_path, monkeypatch) -> None:
    monkeypatch.setenv("HERMES_HUD_REPLAY_DIR", str(tmp_path))
    detail = _detail()

    result = publish_replay(detail, visibility="public")
    publish_dir = tmp_path / "public" / detail.run.replay_id
    manifest = json.loads((publish_dir / "publish.json").read_text(encoding="utf-8"))

    assert result.ok is True
    assert (publish_dir / "replay.html").exists()
    assert (publish_dir / "replay.redacted.json").exists()
    assert (publish_dir / "receipt.json").exists()
    assert manifest["visibility"] == "public"
    assert manifest["entry"].endswith("replay.html")
    assert manifest["view_count"] == 0
    assert manifest["fork_count"] == 0
    gallery_html = (tmp_path / "gallery.html").read_text(encoding="utf-8")
    assert "Hermes Replay Gallery" in gallery_html
    assert "No cloud upload was performed" in gallery_html
    assert "Export run" in gallery_html
    gallery = get_replay_gallery()
    assert gallery["entries"][0]["visibility"] == "public"
    assert gallery["entries"][0]["view_count"] == 0
    assert gallery["entries"][0]["fork_count"] == 0
    record_gallery_view(detail, visibility="public")
    export_fork_json(detail)
    updated_gallery = get_replay_gallery()
    assert updated_gallery["entries"][0]["view_count"] == 1
    assert updated_gallery["entries"][0]["fork_count"] == 1
    updated_gallery_html = (tmp_path / "gallery.html").read_text(encoding="utf-8")
    assert "views 1 · forks 1" in updated_gallery_html

    unpublished = unpublish_replay(detail, visibility="public")
    assert unpublished.ok is True
    assert not publish_dir.exists()
    assert get_replay_gallery()["entries"] == []
    assert "No published replays" in (tmp_path / "gallery.html").read_text(encoding="utf-8")


