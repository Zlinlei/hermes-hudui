def test_export_json_markdown_html_and_fork_use_redacted_payloads(tmp_path, monkeypatch) -> None:
    monkeypatch.setenv("HERMES_HUD_REPLAY_DIR", str(tmp_path))
    detail = _detail()

    results = [
        export_json(detail),
        export_markdown(detail),
        export_html(detail),
        export_clip_html(detail),
        export_share_card_png(detail),
        export_fork_json(detail),
    ]

    for result in results:
        assert result.ok is True
        path = tmp_path / "runs" / detail.run.replay_id / result.export_path.split("/")[-1]
        if path.suffix == ".png":
            image_bytes = path.read_bytes()
            assert image_bytes.startswith(b"\x89PNG\r\n\x1a\n")
            assert b"person@example.com" not in image_bytes
            assert b"/home/joey" not in image_bytes
            continue
        text = path.read_text(encoding="utf-8")
        assert "Generated by Hermes Replay" in text or "Private data removed" in text
        assert "person@example.com" not in text
        assert "/home/joey" not in text

    fork = json.loads((tmp_path / "runs" / detail.run.replay_id / "fork.json").read_text(encoding="utf-8"))
    assert fork["schema_version"] == "0.1"
    assert fork["source"] == "hermes-replay"

    receipt = json.loads((tmp_path / "runs" / detail.run.replay_id / "receipt.json").read_text(encoding="utf-8"))
    index = json.loads((tmp_path / "index.json").read_text(encoding="utf-8"))

    assert receipt["replay_id"] == detail.run.replay_id
    assert index["schema_version"] == "0.1"
    assert index["runs"][0]["replay_id"] == detail.run.replay_id
    index_text = json.dumps(index)
    assert "person@example.com" not in index_text
    assert "/home/joey" not in index_text


def test_share_card_exports_optional_formats(tmp_path, monkeypatch) -> None:
    from PIL import Image

    monkeypatch.setenv("HERMES_HUD_REPLAY_DIR", str(tmp_path))
    detail = _detail()

    expected = {
        "wide": (1200, 630, "share-card.png"),
        "landscape": (1600, 900, "share-card-1600x900.png"),
        "square": (1080, 1080, "share-card-square.png"),
        "story": (1080, 1920, "share-card-story.png"),
    }

    for card_format, (width, height, filename) in expected.items():
        result = export_share_card_png(detail, card_format=card_format)
        path = tmp_path / "runs" / detail.run.replay_id / filename

        assert result.ok is True
        assert result.export_path.endswith(filename)
        with Image.open(path) as image:
            assert image.size == (width, height)


