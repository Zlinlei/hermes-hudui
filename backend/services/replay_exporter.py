REDACTION_NOTICE = "Generated by Hermes Replay. Sensitive fields redacted in Safe Share Mode."
DEFAULT_SETTINGS = {
    "schema_version": "0.1",
    "local_only": True,
    "export_dir": None,
    "safe_share_mode": True,
    "include_raw_logs": False,
    "include_screenshots": False,
}


def default_replay_dir() -> Path:
    hermes_home = Path(default_hermes_dir())
    root = os.environ.get("HERMES_HUD_REPLAY_DIR")
    if root:
        return Path(root).expanduser()
    return hermes_home.parent / ".hermes-hud" / "replays"


def get_replay_settings() -> dict:
    root = default_replay_dir()
    path = root / "settings.json"
    if not path.exists():
        return {**DEFAULT_SETTINGS, "export_dir": str(root)}
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {**DEFAULT_SETTINGS, "export_dir": str(root)}
    settings = {**DEFAULT_SETTINGS, **{key: data.get(key) for key in DEFAULT_SETTINGS if key in data}}
    settings["export_dir"] = str(root)
    settings["local_only"] = True
    settings["safe_share_mode"] = True
    return settings


def update_replay_settings(settings: dict) -> dict:
    root = default_replay_dir()
    root.mkdir(parents=True, exist_ok=True)
    current = get_replay_settings()
    allowed = {
        "safe_share_mode": True,
        "include_raw_logs": bool(settings.get("include_raw_logs", current["include_raw_logs"])),
        "include_screenshots": bool(settings.get("include_screenshots", current["include_screenshots"])),
    }
    next_settings = {
        **DEFAULT_SETTINGS,
        **allowed,
        "local_only": True,
        "export_dir": str(root),
    }
    _write_text(root / "settings.json", json.dumps(next_settings, indent=2, sort_keys=True))
    return next_settings


def redacted_detail(detail: ReplayDetail) -> ReplayDetail:
    redacted = scan_replay(detail)
    for finding in redacted.redactions:
        finding.preview = finding.replacement
    if redacted.receipt:
        receipt_payload = to_dict(redacted.receipt)
        receipt_payload["hashes"]["receipt_hash"] = None
        redacted.receipt.hashes.receipt_hash = _hash_payload(receipt_payload)
        redacted.run.hashes.receipt_hash = redacted.receipt.hashes.receipt_hash
    return redacted


def _run_dir(detail: ReplayDetail) -> Path:
    path = default_replay_dir() / "runs" / detail.run.replay_id
    path.mkdir(parents=True, exist_ok=True)
    return path


def _write_text(path: Path, content: str) -> None:
    path.write_text(content, encoding="utf-8")


def _update_publish_manifests(replay_id: str, updater) -> None:
    root = default_replay_dir()
    for visibility in ("public", "unlisted"):
        manifest_path = root / visibility / replay_id / "publish.json"
        if not manifest_path.exists():
            continue
        try:
            manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            continue
        if not isinstance(manifest, dict):
            continue
        updater(manifest)
        _write_text(manifest_path, json.dumps(manifest, indent=2, sort_keys=True))


def _write_receipt(detail: ReplayDetail, run_dir: Path) -> None:
    if detail.receipt is None:
        return
    receipt = to_dict(detail.receipt)
    receipt.update(sign_receipt_hashes(
        detail.run.hashes.receipt_hash or "",
        detail.run.hashes.redacted_replay_hash or "",
        default_replay_dir(),
    ))
    _write_text(run_dir / "receipt.json", json.dumps(receipt, indent=2, sort_keys=True))


def _update_index(detail: ReplayDetail, exported_path: Path) -> None:
    root = default_replay_dir()
    root.mkdir(parents=True, exist_ok=True)
    index_path = root / "index.json"
    try:
        index = json.loads(index_path.read_text(encoding="utf-8")) if index_path.exists() else {"schema_version": "0.1", "runs": []}
    except (json.JSONDecodeError, OSError):
        index = {"schema_version": "0.1", "runs": []}
    runs = [item for item in index.get("runs", []) if item.get("replay_id") != detail.run.replay_id]
    runs.insert(0, {
        "replay_id": detail.run.replay_id,
        "source_session_id": detail.run.source_session_id,
        "title": detail.run.title,
        "status": detail.run.status,
        "started_at": detail.run.started_at,
        "updated_at": detail.run.updated_at,
        "redaction_status": detail.run.redaction_status,
        "receipt_hash": detail.run.hashes.receipt_hash,
        "redacted_replay_hash": detail.run.hashes.redacted_replay_hash,
        "last_export_path": str(exported_path),
    })
    index["runs"] = runs[:250]
    _write_text(index_path, json.dumps(index, indent=2, sort_keys=True))


def _update_skill_index(detail: ReplayDetail) -> None:
    root = default_replay_dir()
    root.mkdir(parents=True, exist_ok=True)
    path = root / "skills.json"
    try:
        index = json.loads(path.read_text(encoding="utf-8")) if path.exists() else {"schema_version": "0.1", "skills": []}
    except (json.JSONDecodeError, OSError):
        index = {"schema_version": "0.1", "skills": []}
    skills = {item.get("name"): item for item in index.get("skills", []) if item.get("name")}
    receipt_skills = detail.receipt.skills_used if detail.receipt else []
    mutation_events = [
        event for event in detail.events
        if event.type in {"skill_created", "skill_modified"} and event.metadata.get("skill_name")
    ]
    for skill in receipt_skills:
        name = skill.get("name")
        if not name:
            continue
        entry = skills.setdefault(name, {
            "name": name,
            "version": skill.get("version"),
            "hash": skill.get("hash"),
            "runs_used_in": [],
            "usage_count": 0,
            "success_count": 0,
            "success_rate": None,
            "first_used_at": detail.run.started_at or detail.run.created_at,
            "last_used_at": detail.run.started_at or detail.run.updated_at,
            "mutation_receipts": [],
        })
        already_indexed = detail.run.replay_id in entry["runs_used_in"]
        if detail.run.replay_id not in entry["runs_used_in"]:
            entry["runs_used_in"].insert(0, detail.run.replay_id)
        if entry.get("first_used_at") is None:
            entry["first_used_at"] = detail.run.started_at or detail.run.created_at
        entry["last_used_at"] = detail.run.started_at or detail.run.updated_at or entry.get("last_used_at")
        entry["usage_count"] = len(entry["runs_used_in"])
        entry["success_count"] = int(entry.get("success_count") or 0)
        if detail.run.status == "success" and not already_indexed:
            entry["success_count"] += 1
        entry["success_rate"] = round(entry["success_count"] / entry["usage_count"], 4) if entry["usage_count"] else None
        if entry.get("hash") is None and skill.get("hash"):
            entry["hash"] = skill.get("hash")
        if entry.get("version") is None and skill.get("version"):
            entry["version"] = skill.get("version")
        for event in mutation_events:
            if event.metadata.get("skill_name") != name:
                continue
            mutation_receipt = {
                "event_id": event.event_id,
                "event_type": event.type,
                "replay_id": detail.run.replay_id,
                "source_session_id": detail.run.source_session_id,
                "timestamp": event.timestamp,
                "receipt_hash": detail.run.hashes.receipt_hash,
                "redacted_replay_hash": detail.run.hashes.redacted_replay_hash,
            }
            receipts = entry.setdefault("mutation_receipts", [])
            if not any(item.get("event_id") == event.event_id for item in receipts):
                receipts.insert(0, mutation_receipt)
    public_skills = []
    for entry in skills.values():
        clean = dict(entry)
        clean.pop("_successful_runs", None)
        public_skills.append(clean)
    index["skills"] = sorted(public_skills, key=lambda item: item["usage_count"], reverse=True)
    _write_text(path, json.dumps(index, indent=2, sort_keys=True))


def get_skill_provenance_index() -> dict:
    path = default_replay_dir() / "skills.json"
    if not path.exists():
        return {"schema_version": "0.1", "skills": []}
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {"schema_version": "0.1", "skills": []}
    return data if isinstance(data, dict) else {"schema_version": "0.1", "skills": []}


def get_replay_gallery() -> dict:
    root = default_replay_dir()
    entries = []
    for visibility in ("public", "unlisted"):
        directory = root / visibility
        if not directory.exists():
            continue
        for manifest_path in sorted(directory.glob("*/publish.json")):
            try:
                manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
            except (json.JSONDecodeError, OSError):
                continue
            if isinstance(manifest, dict):
                entries.append({
                    "visibility": visibility,
                    "manifest_path": str(manifest_path),
                    "replay_id": manifest.get("replay_id"),
                    "title": manifest.get("title"),
                    "entry": manifest.get("entry"),
                    "receipt_hash": manifest.get("receipt_hash"),
                    "redacted_replay_hash": manifest.get("redacted_replay_hash"),
                    "view_count": int(manifest.get("view_count") or 0),
                    "fork_count": int(manifest.get("fork_count") or 0),
                })
    return {"schema_version": "0.1", "entries": entries}


