def _finalize_export(path: Path, detail: ReplayDetail) -> ReplayExportResult:
    run_dir = _run_dir(detail)
    _write_receipt(detail, run_dir)
    _update_index(detail, path)
    _update_skill_index(detail)
    return _export_result(path, detail)


def _write(path: Path, content: str, mode: str = "w") -> ReplayExportResult:
    _write_text(path, content)
    return ReplayExportResult(
        ok=True,
        export_path=str(path),
        receipt_hash=None,
        redacted_replay_hash=None,
        redaction_notice=REDACTION_NOTICE,
    )


def _export_result(path: Path, detail: ReplayDetail) -> ReplayExportResult:
    return ReplayExportResult(
        ok=True,
        export_path=str(path),
        receipt_hash=detail.run.hashes.receipt_hash,
        redacted_replay_hash=detail.run.hashes.redacted_replay_hash,
        redaction_notice=REDACTION_NOTICE,
    )


def export_json(detail: ReplayDetail) -> ReplayExportResult:
    redacted = redacted_detail(detail)
    path = _run_dir(redacted) / "replay.redacted.json"
    payload = {
        "schema_version": "0.1",
        "redaction_notice": REDACTION_NOTICE,
        "replay": to_dict(redacted),
    }
    _write_text(path, json.dumps(payload, indent=2, sort_keys=True))
    return _finalize_export(path, redacted)


def _timeline_markdown(detail: ReplayDetail) -> str:
    rows = []
    for event in detail.events:
        rows.append(f"- **{event.title}** ({event.type}): {event.summary}")
    return "\n".join(rows) or "- No timeline events detected."


def _artifacts_markdown(detail: ReplayDetail) -> str:
    rows = []
    for artifact in detail.artifacts:
        rows.append(f"- **{artifact.title}** ({artifact.type}): {artifact.summary or 'No summary.'}")
    return "\n".join(rows) or "- No proof artifacts detected."


