def export_fork_json(detail: ReplayDetail) -> ReplayExportResult:
    redacted = redacted_detail(detail)
    path = _run_dir(redacted) / "fork.json"
    prompt = next((event.summary for event in redacted.events if event.type == "prompt"), redacted.run.title)
    payload = {
        "schema_version": "0.1",
        "source": "hermes-replay",
        "run_title": redacted.run.title,
        "original_prompt_redacted": prompt,
        "recommended_model": redacted.run.primary_model,
        "skills": redacted.receipt.skills_used if redacted.receipt else [],
        "toolsets": sorted({str(event.metadata.get("tool_name")) for event in redacted.events if event.metadata.get("tool_name")}),
        "project_context_summary": "Private project context removed.",
        "steps": [event.summary for event in redacted.events if event.type in {"prompt", "assistant_message", "tool_call", "terminal_command"}][:20],
        "redaction_notice": "Private data removed.",
    }
    _write_text(path, json.dumps(payload, indent=2, sort_keys=True))
    _update_publish_manifests(redacted.run.replay_id, lambda manifest: manifest.update({
        "fork_count": int(manifest.get("fork_count") or 0) + 1,
    }))
    _write_gallery_index()
    return _finalize_export(path, redacted)


SHARE_CARD_FORMATS = {
    "wide": (1200, 630, "share-card.png"),
    "landscape": (1600, 900, "share-card-1600x900.png"),
    "square": (1080, 1080, "share-card-square.png"),
    "story": (1080, 1920, "share-card-story.png"),
}


def export_share_card_png(detail: ReplayDetail, card_format: str = "wide") -> ReplayExportResult:
    """Generate a social share card PNG from the redacted receipt."""
    try:
        from PIL import Image, ImageDraw, ImageFont
    except ImportError as exc:
        raise RuntimeError("Pillow is required to generate PNG share cards") from exc

    redacted = redacted_detail(detail)
    width, height, filename = SHARE_CARD_FORMATS.get(card_format, SHARE_CARD_FORMATS["wide"])
    path = _run_dir(redacted) / filename

    bg = (7, 18, 17)
    panel = (12, 34, 32)
    primary = (94, 246, 210)
    text = (215, 255, 251)
    dim = (143, 170, 166)
    border = (31, 79, 74)
    success = (86, 220, 146)

    image = Image.new("RGB", (width, height), bg)
    draw = ImageDraw.Draw(image)

    def font(size: int, bold: bool = False):
        candidates = [
            "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf" if bold else "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf",
            "/usr/share/fonts/truetype/liberation2/LiberationSans-Bold.ttf" if bold else "/usr/share/fonts/truetype/liberation2/LiberationSans-Regular.ttf",
        ]
        for candidate in candidates:
            if Path(candidate).exists():
                return ImageFont.truetype(candidate, size)
        return ImageFont.load_default()

    scale = min(width / 1200, height / 630)
    margin = max(42, int(48 * scale))
    title_font = font(max(38, int(56 * scale)), True)
    label_font = font(max(18, int(22 * scale)))
    metric_font = font(max(26, int(34 * scale)), True)
    mono_font = font(max(16, int(20 * scale)))

    draw.rectangle((margin, margin, width - margin, height - margin), fill=panel, outline=border, width=2)
    draw.text((margin + 34, margin + 30), "HERMES REPLAY", fill=primary, font=label_font)

    title = shorten(redacted.run.title, width=42 if height > width else 54, placeholder="...")
    title_y = margin + 78
    draw.text((margin + 34, title_y), title, fill=text, font=title_font)

    status_color = success if redacted.run.status == "success" else primary
    status_y = title_y + int(92 * scale)
    draw.rounded_rectangle((margin + 34, status_y, margin + 210, status_y + 48), radius=6, outline=status_color, width=2)
    draw.text((margin + 60, status_y + 10), redacted.run.status.upper(), fill=status_color, font=label_font)

    tests_total = (redacted.run.counts.tests_passed or 0) + (redacted.run.counts.tests_failed or 0)
    metrics = [
        ("Duration", f"{round((redacted.run.duration_ms or 0) / 1000)}s" if redacted.run.duration_ms else "Unknown"),
        ("Cost", f"${redacted.run.total_cost_usd:.4f}" if redacted.run.total_cost_usd is not None else "Unknown"),
        ("Tool calls", str(redacted.run.counts.tool_calls)),
        ("Skills", str(redacted.run.counts.skills_used)),
        ("Files", str(redacted.run.counts.files_changed) if redacted.run.counts.files_changed is not None else "Unknown"),
        ("Tests", f"{redacted.run.counts.tests_passed}/{tests_total} passed" if tests_total else "Not detected"),
    ]
    columns = 2 if height >= width else 3
    metric_area_width = width - (margin * 2) - 68
    column_width = max(220, metric_area_width // columns)
    x_positions = [margin + 34 + (column_width * index) for index in range(columns)]
    metric_start_y = status_y + int(94 * scale)
    row_gap = max(112, int(112 * scale))
    for index, (label, value) in enumerate(metrics):
        x = x_positions[index % columns]
        y = metric_start_y + row_gap * (index // columns)
        draw.text((x, y), label.upper(), fill=dim, font=label_font)
        draw.text((x, y + 34), value, fill=primary, font=metric_font)

    replay_hash = redacted.run.hashes.redacted_replay_hash or "hash pending"
    footer_y = height - margin - 66
    draw.line((margin + 34, footer_y - 20, width - margin - 34, footer_y - 20), fill=border, width=1)
    draw.text((margin + 34, footer_y), f"Replay hash: {replay_hash}", fill=dim, font=mono_font)
    draw.text((width - margin - 190, footer_y), "View replay", fill=primary, font=mono_font)

    image.save(path, format="PNG", optimize=True)
    return _finalize_export(path, redacted)


def publish_replay(detail: ReplayDetail, visibility: str = "unlisted") -> ReplayExportResult:
    """Publish a replay to a local static public/unlisted directory."""
    if visibility not in {"public", "unlisted"}:
        visibility = "unlisted"
    redacted = redacted_detail(detail)
    run_dir = _run_dir(redacted)
    if not (run_dir / "replay.html").exists():
        export_html(detail)
    if not (run_dir / "replay.redacted.json").exists():
        export_json(detail)
    if not (run_dir / "receipt.json").exists():
        _write_receipt(redacted, run_dir)

    target = default_replay_dir() / visibility / redacted.run.replay_id
    target.mkdir(parents=True, exist_ok=True)
    for name in ["replay.html", "replay.redacted.json", "receipt.json", "share-card.png", "replay.md", "fork.json"]:
        src = run_dir / name
        if src.exists():
            dst = target / name
            if src.suffix == ".png":
                dst.write_bytes(src.read_bytes())
            else:
                _write_text(dst, src.read_text(encoding="utf-8"))
    manifest = {
        "schema_version": "0.1",
        "source": "hermes-replay",
        "visibility": visibility,
        "replay_id": redacted.run.replay_id,
        "title": redacted.run.title,
        "entry": str(target / "replay.html"),
        "receipt_hash": redacted.run.hashes.receipt_hash,
        "redacted_replay_hash": redacted.run.hashes.redacted_replay_hash,
        "view_count": 0,
        "fork_count": 0,
    }
    manifest_path = target / "publish.json"
    _write_text(manifest_path, json.dumps(manifest, indent=2, sort_keys=True))
    _write_gallery_index()
    return _export_result(manifest_path, redacted)


def unpublish_replay(detail: ReplayDetail, visibility: str = "unlisted") -> ReplayExportResult:
    """Remove a locally published replay from public/unlisted directories."""
    if visibility not in {"public", "unlisted"}:
        visibility = "unlisted"
    target = default_replay_dir() / visibility / detail.run.replay_id
    if target.exists():
        shutil.rmtree(target)
    _write_gallery_index()
    return ReplayExportResult(
        ok=True,
        export_path=str(target),
        receipt_hash=detail.run.hashes.receipt_hash,
        redacted_replay_hash=detail.run.hashes.redacted_replay_hash,
        redaction_notice="Local publish directory removed.",
    )


def record_gallery_view(detail: ReplayDetail, visibility: str = "unlisted") -> ReplayExportResult:
    """Increment the local view counter for a published replay manifest."""
    if visibility not in {"public", "unlisted"}:
        visibility = "unlisted"
    manifest_path = default_replay_dir() / visibility / detail.run.replay_id / "publish.json"
    if not manifest_path.exists():
        return ReplayExportResult(
            ok=False,
            export_path=str(manifest_path),
            receipt_hash=detail.run.hashes.receipt_hash,
            redacted_replay_hash=detail.run.hashes.redacted_replay_hash,
            redaction_notice="Replay is not locally published.",
        )

    def increment(manifest: dict) -> None:
        manifest["view_count"] = int(manifest.get("view_count") or 0) + 1

    _update_publish_manifests(detail.run.replay_id, increment)
    _write_gallery_index()
    return ReplayExportResult(
        ok=True,
        export_path=str(manifest_path),
        receipt_hash=detail.run.hashes.receipt_hash,
        redacted_replay_hash=detail.run.hashes.redacted_replay_hash,
        redaction_notice="Local gallery view recorded.",
    )


